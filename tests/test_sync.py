# ──────────────────────────── default ignore list ────────────────────────────


def test_default_sync_ignore_excludes_secrets_and_volatility():
    """Privacy + noise guard: the default exclude list MUST include the
    known per-machine / sensitive paths. Regression on this is the
    kind of bug that ships an API key to a private GitHub repo."""
    rules = "\n".join(DEFAULT_SYNC_IGNORE)
    assert "usage.jsonl" in rules
    assert "cron-logs" in rules
    assert "cron/" in rules
    assert "bandit.sqlite" in rules


