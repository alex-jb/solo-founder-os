"""Tests for solo_founder_os.sync — multi-machine git-based sync.

git is mocked via subprocess.run so tests don't actually network.
"""
from __future__ import annotations
import os
import sys

import pytest

sys.path.insert(0, os.path.join(os.path.dirname(__file__), ".."))

import solo_founder_os.sync as syncmod
from solo_founder_os.sync import (
    DEFAULT_SYNC_IGNORE,
    cmd_init,
    cmd_pull,
    cmd_push,
    cmd_status,
    main,
)


@pytest.fixture(autouse=True)
def _isolate_sync_root(tmp_path, monkeypatch):
    """Redirect SYNC_ROOT into tmp_path so tests don't touch real ~/.solo-founder-os/."""
    monkeypatch.setattr(syncmod, "SYNC_ROOT", tmp_path / ".solo-founder-os")


def _stub_git(monkeypatch, *,
                returncode: int = 0,
                output: str = "",
                track: list | None = None,
                rc_for: dict | None = None):
    """Replace _git with a fake. `rc_for` lets tests vary returncode by
    first arg (the git subcommand)."""
    def fake(*args, cwd=None, capture=True):
        if track is not None:
            track.append(args)
        if rc_for is not None and args and args[0] in rc_for:
            return (rc_for[args[0]], output)
        return (returncode, output)
    monkeypatch.setattr(syncmod, "_git", fake)


def _ns(**kwargs):
    """argparse.Namespace builder for direct cmd_* calls."""
    import argparse
    return argparse.Namespace(**kwargs)


# ──────────────────────────── init ────────────────────────────


def test_init_creates_sync_root_and_gitignore(monkeypatch, tmp_path, capsys):
    _stub_git(monkeypatch)
    rc = cmd_init(_ns(remote="git@github.com:x/y.git", force=False))
    assert rc == 0
    sr = syncmod.SYNC_ROOT
    assert sr.exists()
    gi = sr / ".gitignore"
    assert gi.exists()
    body = gi.read_text()
    # Must include the per-machine state we deliberately exclude.
    assert "usage.jsonl" in body
    assert "cron-logs/" in body
    assert "bandit.sqlite" in body


def test_init_idempotent_skips_when_already_repo(monkeypatch, tmp_path, capsys):
    """Re-running init on an existing repo without --force should refuse."""
    sr = syncmod.SYNC_ROOT
    sr.mkdir(parents=True)
    (sr / ".git").mkdir()  # simulate existing repo
    _stub_git(monkeypatch)
    rc = cmd_init(_ns(remote=None, force=False))
    assert rc == 1
    err = capsys.readouterr().err
    assert "Already a git repo" in err


def test_init_force_rewrites_gitignore(monkeypatch, tmp_path, capsys):
    sr = syncmod.SYNC_ROOT
    sr.mkdir(parents=True)
    (sr / ".git").mkdir()
    (sr / ".gitignore").write_text("# old custom rules\n")
    _stub_git(monkeypatch)
    rc = cmd_init(_ns(remote="git@github.com:x/y.git", force=True))
    assert rc == 0
    body = (sr / ".gitignore").read_text()
    assert "Auto-generated by sfos-sync" in body
    assert "# old custom rules" not in body


def test_init_calls_git_init_when_no_repo(monkeypatch, tmp_path):
    calls: list = []
    _stub_git(monkeypatch, track=calls)
    cmd_init(_ns(remote="git@github.com:x/y.git", force=False))
    # Track args[0] for the subcommand
    cmds = [a[0] for a in calls if a]
    assert "init" in cmds
    assert "remote" in cmds


