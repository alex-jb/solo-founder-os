def test_push_no_changes_still_attempts_push(monkeypatch, tmp_path, capsys):
    """Clean working tree → no commit, but still try push (might be
    behind on previously committed work)."""
    sr = syncmod.SYNC_ROOT
    sr.mkdir(parents=True)
    (sr / ".git").mkdir()
    calls: list = []
    # diff --cached --quiet returns 0 when no staged changes
    _stub_git(monkeypatch, returncode=0, track=calls)
    rc = cmd_push(_ns(message=None))
    assert rc == 0
    cmds = [a[0] for a in calls if a]
    assert "add" in cmds
    assert "diff" in cmds
    assert "push" in cmds  # tries push even with no commit
    assert "commit" not in cmds  # but no commit


def test_push_with_changes_commits_and_pushes(monkeypatch, tmp_path, capsys):
    sr = syncmod.SYNC_ROOT
    sr.mkdir(parents=True)
    (sr / ".git").mkdir()
    calls: list = []
    # diff --cached --quiet returns 1 when there ARE staged changes
    _stub_git(monkeypatch,
                rc_for={"diff": 1},  # changes exist
                track=calls)
    rc = cmd_push(_ns(message="weekly drift report"))
    assert rc == 0
    cmds = [a[0] for a in calls if a]
    assert "commit" in cmds
    # Look for the message in the commit args
    commit_call = next(a for a in calls if a and a[0] == "commit")
    assert "weekly drift report" in commit_call


def test_push_default_commit_message_includes_hostname(monkeypatch, tmp_path):
    sr = syncmod.SYNC_ROOT
    sr.mkdir(parents=True)
    (sr / ".git").mkdir()
    calls: list = []
